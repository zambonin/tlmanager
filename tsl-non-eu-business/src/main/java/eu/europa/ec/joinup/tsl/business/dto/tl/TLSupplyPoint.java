package eu.europa.ec.joinup.tsl.business.dto.tl;

public class TLSupplyPoint extends AbstractTLDTO {

    private String value;

    public TLSupplyPoint() {
    }

    public TLSupplyPoint(int iddb, String location, String supply) {
        super(iddb, location);
        this.setValue(supply);
    }

    public String asTSLTypeV5() {
        return value;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = (prime * result) + ((value == null) ? 0 : value.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        TLSupplyPoint other = (TLSupplyPoint) obj;
        if (value == null) {
            if (other.value != null) {
                return false;
            }
        } else if (!value.equals(other.value)) {
            return false;
        }
        return true;
    }

}
